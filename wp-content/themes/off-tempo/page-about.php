        <h3>About</h3>
        <p>Off Tempo is a multi-media archive project predominantly covering music in Seattle and other music hotbeds of Northwestern Washington. The project is focuses on local and touring musicians performing in alternative venues in all-ages / DIY communities.</p>
        <p>Off Tempo began as a loose project that documented local music in the Tri-Cities, Washington,  from 2007 - 2008 at the 321 Art Space. Recording resumed in 2008 - 2009 in Bellingham, Seattle and in other cities across the United States. Since early 2009 to the present day most documentation and recording takes place in the Pacific Northwest</p>
        <p>All of the audio recordings are done in all-ages spaces (houses, shops, galleries, DIY venues) and are submitted to and aired on <strong><a href="http://www.hollowearthradio.org" target="_blank">Hollow Earth Radio</a></strong>, a local online community radio station based in Seattle, WA. </p>
        <h3>Mission</h3>
		<p>Off Tempo raises awareness of the underground music community by documenting local and touring bands in Washington State. Off Tempo exists online as a website to showcase this lesser-known music, provide bands with quality video and audio recordings and exposure to a wider audience.</p>
		<p>Off Tempo is not in the business of continuing the trend of rating art with points.</p>
		<p>Off Tempo is a <i>free</i> public resource.</p>
        <h3>If You are an Artist or Musician...</h3>
        <p>Live recordings on Off Tempo are free to download or stream upon the artists agreement.</p>
        <p>If we have recorded audio of a performance, most of the time we have either verbal or written consent from the artist(s) to provide said audio as a free service for the public.</p>
		<p>If you are a musician whose recorded material is publicly available on our website- who has not consented to its availability- please <a href="http://www.offtempo.com/contact/">contact us</a>.</p>
